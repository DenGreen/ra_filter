function Portfolio() {
  const filters = ["All", "Websites", "Flayers", "Business Cards"];
  let [selected, setSelected] = useState("All");

  const onSelectFilter = (event) => {
    setSelected(selected = event.target.dataset.filter);
  };

  let arrImgFilter = arrImg.filter(value =>{
    return value.category === selected;
  })

  const imgOutput = () =>{
    return selected === "All" ? arrImg : arrImgFilter;
  }

  return (
    <div>
      <Toolbar
      filters={filters}
      selected={selected}
      onSelectFilter={onSelectFilter}
    />
    <ProjectList arrImg={imgOutput()}/>
    </div>
  );
}

export default Portfolio;
